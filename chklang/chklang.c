#include "hrbstd.h"

void HariMain(){
    int langmode = api_check_langmode();
    static char str_sjis[23] = {
        0x93, 0xfa, 0x96, 0x7b, 0x8c, 0xea, 0x83, 0x56, 0x83, 0x74, 0x83, 0x67,
        0x4a, 0x49, 0x53, 0x83, 0x82, 0x81, 0x5b, 0x83, 0x68, 0x0a, 0x00
    };
    static char str_euc_jp[17] = {
        0xc6, 0xfc, 0xcb, 0xdc, 0xb8, 0xec, 0x45, 0x55, 0x43, 0xa5, 0xe2, 0xa1,
        0xbc, 0xa5, 0xc9, 0x0a, 0x00
    };

    if(langmode == 0){
        api_putstr("English EUC Mode\n");
    }else if(langmode == 1){
        api_putstr(str_sjis);
    }else if(langmode == 2){
        api_putstr(str_euc_jp);
    }

    api_putchar('\n');
    api_end();
}